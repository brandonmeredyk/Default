package Engine;

/**
 *
 * @author Brandon
 */
public class Dimension2D {

    float Width, Height;
    
    public Dimension2D(){
        
        Width = 0F;
        Height = 0F;
        
    }
    
    public Dimension2D(Dimension2D vect){
        Width = vect.Width;
        Height = vect.Height;
    }
    
    public Dimension2D(float x, float y){
        Width = x;
        Height = y;
    }
    
    
    
    
}
